    family = "serif",
    xlab = "Longitude",
    ylab = "Latitude",
    main = " ",
    cex.lab = 6.5,
    cex.axis = 6.5)
  maps::map('worldHires',
            add = T,
            col = 'antiquewhite4',
            fill = T)
