                                      "#f9a242b2",
                                      "#f9b641b2",
                                      "#f7cb44b2",
                                      "#efe350b2",
                                      "#e8fa5bb2")))
  myvis_gam(
    gam[[2]],
    view = c('lon', 'lat'),
    too.far = 0.03,
    plot.type = 'contour',
    contour.col = contour_col,
    color = "jet" ,
    type = 'response',
